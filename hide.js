function show1() {
    var show1 = document.getElementById('show1');
    var hide1 = document.getElementById('hide1');
    if (hide1.style.display === 'none') {
          hide1.style.display = 'block';
          show1.innerText = "點擊隱藏";
    }
    else {
          hide1.style.display = 'none';
          show1.innerText = "Beat Box";
    }
}

